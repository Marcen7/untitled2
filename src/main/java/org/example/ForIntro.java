package org.example;

public class ForIntro {
    public static void main(String[] args) {
        int i;

        for (i=3; i>2; i+=2 ){
            System.out.println("To jest pętla");
            System.out.println("Aktualana wartość licznika : " + i);
        };
        System.out.println(i);

    }

}
